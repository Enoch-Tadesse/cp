long long modPow(long long base, long long exp, long long mod) {
    long long res = 1;
    base %= mod;

    while (exp > 0) {
        if (exp % 2 == 1) {
            res = (res * base) % mod;
        }

        base = (base * base) % mod;
        exp /= 2;
    }

    return res;
}

typedef long long ll;
typedef vector<ll> vl;

void solve() {
    ll n;
    cin >> n;
    vector<ll> nums(n);
    for (ll& x : nums) cin >> x;

    vll pre(n, 0);
    vll suf(n, 0);

    ll p = 0;

    for (int i = 0; i < n; i++) pre[i] = gcd(p, nums[i]), p = pre[i];
    p = 0;
    for (int i = n - 1; i >= 0; i--) suf[i] = gcd(p, nums[i]), p = suf[i];

    ll ans = pre[n - 1];

    for (int i = 1; i < n - 1; i++) ans += min(pre[i], suf[i]);

    print(ans);
}

